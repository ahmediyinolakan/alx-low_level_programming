/**
 * op_add - compute sum of two ints
 * @a: int to add to b
 * @b: int to add to a
 * Return: sum of a and b
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - compute difference of two ints
 * @a: int to subtract with b
 * @b: int to subtract from a
 * Return: difference of a and b
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - compute product of two ints
 * @a: int to multiply with b
 * @b: int to multiply with a
 * Return: product of a and b
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - compute quotient of two ints
 * @a: int to divide by b
 * @b: int to divide a by
 * Return: quotient of a and b
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - compute remainder of two ints
 * @a: int to mod with b
 * @b: int to mod a by
 * Return: remainder of a and b
 */
int op_mod(int a, int b)
{
	return (a % b);
}
